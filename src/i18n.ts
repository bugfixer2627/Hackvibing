let language: LanguageCode = "en";
const listeners = new Set<() => void>();

function safeGetItem(key: string) {
  try {
    return localStorage.getItem(key);
  } catch {
    return null;
  }
}

function safeSetItem(key: string, value: string) {
  try {
    localStorage.setItem(key, value);
  } catch {
    return;
  }
}

function normalizeLanguage(input: string | null | undefined): LanguageCode | null {
  if (!input) return null;
  const lower = input.toLowerCase();
  if (lower.startsWith("zh")) return "zh";
  if (lower.startsWith("en")) return "en";
  if (lower.startsWith("id")) return "id";
  if (lower.startsWith("hi")) return "hi";
  return null;
}

function initLanguage() {
  const stored = normalizeLanguage(safeGetItem(STORAGE_KEY));
  if (stored) {
    language = stored;
    return;
  }
  const detected = normalizeLanguage(typeof navigator !== "undefined" ? navigator.language : null);
  language = detected ?? "en";
  safeSetItem(STORAGE_KEY, language);
}

initLanguage();

function format(template: string, params?: Record<string, string | number>) {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, key) => {
    const value = params[key];
    return value === undefined || value === null ? `{${key}}` : String(value);
  });
}

export const i18n = {
  t(key: string, params?: Record<string, string | number>) {
    const dict = dictionaries[language] ?? dictionaries.zh;
    const fallback = dictionaries.en[key] ?? dictionaries.zh[key] ?? key;
    const template = dict[key] ?? fallback;
    return format(template, params);
  },
  getLanguage() {
    return language;
  },
  setLanguage(code: string) {
    const normalized = normalizeLanguage(code) ?? "en";
    if (normalized === language) return;
    language = normalized;
    safeSetItem(STORAGE_KEY, language);
    listeners.forEach((listener) => listener());
  },
  subscribe(listener: () => void) {
    listeners.add(listener);
    return () => listeners.delete(listener);
  }
};

export function useI18nLanguage() {
  return useSyncExternalStore(i18n.subscribe, i18n.getLanguage, i18n.getLanguage);
}

